use Vas_MyBase
SELECT ЗАКАЗЫ.id_клиента,
	sum(ТОВАРЫ.Цена * ЗАКАЗЫ.Количество)[Cумма покупок],
	sum(ЗАКАЗЫ.Количество)[Колличество купленных товаров]
FROM ЗАКАЗЫ 
inner join ТОВАРЫ on ЗАКАЗЫ.id_товара = Товары.id_товара
group by заказы.id_клиента
having  sum(ТОВАРЫ.Цена * ЗАКАЗЫ.Количество) < 100000
		and sum(ТОВАРЫ.Цена * ЗАКАЗЫ.Количество) > 70000