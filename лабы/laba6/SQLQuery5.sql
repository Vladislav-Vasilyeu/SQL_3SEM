USE Vas_MyBase
SELECT КЛИЕНТЫ.Наименование_фирмы_заказчика,
		SUM(ЗАКАЗЫ.Количество)[Кол-во купленных деревянных товаров]
FROM ЗАКАЗЫ
	INNER JOIN КЛИЕНТЫ ON ЗАКАЗЫ.id_клиента = КЛИЕНТЫ.id_клиента
	INNER JOIN ТОВАРЫ ON ТОВАРЫ.id_товара = ЗАКАЗЫ.id_товара
WHERE ТОВАРЫ.Описание_товара LIKE '%деревян%'
GROUP BY КЛИЕНТЫ.id_клиента, КЛИЕНТЫ.Наименование_фирмы_заказчика